package PRBAS;

public class Employee {
	private String name;
	private String id;
	
	public Employee(String name , String id ) {
		this.name = name;
		this.id   = id;
	}
	
	public String getID() {
		return id;
	}
	
	public String getName() {
		return name;
	}
	
	public String toString() {
		return name;
	}


}
